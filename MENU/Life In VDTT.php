<p align="justify">There are enough joints in and around the campus to cool your heels off.</p>
            <p align="justify">For those who are Veggy buffs.. and are feeling lazy can go Shefali or Rainbow just in front of IIT Main gate. At Shefali they serve a great thali and otherwise too the food is delicious. Rainbow is a once in a while try-out and there you can try out the shakes and pastries and cakes for the Birthdays.</p>
            <p align="justify">For Non-Veg Stuff, you can always try the kathi-kabas served at the SDA market. Also delicious biryani's and roasted chikens are served at open restraints in Saket market. COSY also specializes in this area and is just at 2km reach from the campus</p>
