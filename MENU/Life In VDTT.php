            <p align="justify">The nights are creepy and fun filled, everyone waits for some one to call him/ (rare) her for a cup of tea, and soon the 5 mins tea break extends to a hour long chit-chats that you are bound to remember through your life.</p>
            <p align="justify">The IPL/FIFA/EPL  sessions, jokes that go around and the background songs keep the atmosphere of the lab light and constructive. There are arrangements too for those who want to stretch their legs to relax.</p>
            <p align="justify">Behind all this, there is a great amount of effort everyone put towards the academics and its not wrong to say that</p>
