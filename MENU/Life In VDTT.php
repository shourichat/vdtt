        <br /><br />      

<p align="justify">There is a famous saying<strong><em>&quot;All that starts well.... Ends well &quot;</em></strong>, but none talk about the eternity that lies between the two. Other than making our dream of entering the prestigious cult of VLSI designers, life at IIT-Delhi offers much more, if you dare to experience it. Let us share some words, to give a glimpse of how, is it to be a part of VDTT.</p>
            <p align="justify">The first few days at IIT, one would feel like &quot;life to chill hai yaar&quot;(life is chill!!!), But beware and don't get carried away, the countless deadlines will soon strike you one by one. There starts the true journey of your dream life. You will be astonished to see the gamut to which you can push your abilities and, realize that sleep becomes your favorite time pass. There is another saying<strong><em>&quot; With greater pain .....comes greater pleasure &quot;</em></strong></p>

