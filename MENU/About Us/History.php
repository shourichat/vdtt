      	<h2>History of VDTT</h2>
        <br /><br />      

<p align="justify">
        The VLSI Design Tools and Technology Program aims at industrially relevant cutting-edge research and promoting academic growth by offering a state-of-the-art Masters program. The VDTT program undertakes collaborative projects that offer opportunities for long-term interaction between academia and industry. An important objective of the program is to foster the development of capable leaders exposed to the breadth of design techniques, algorithms and tools and technology issues in the field of VLSI.<br /><br />   

The VDTT program has been operational since July 1996, and is one of the premier programs offered in the country. In this program industries working on the cutting edge in VLSI design and EDA, sponsor students selected from amongst the exceptionally meritorious applicants. The average GATE (Graduate Aptitude Test in Engineering) percentile of candidates making it to the interviews has been over 99.7 in the last four years.<br /><br />   

A unique feature of the VDTT program at IIT Delhi is that the Department of Electrical Engineering, the Department of Computer Science and Engineering and Center for Applied Research in Electronics (CARE) jointly run the program, endowing it with a truly interdisciplinary and rich character. Faculty in the participating departments and centre have an excellent track record in teaching and research. The students are exposed to faculty with expertise in a diverse range of topics such as Device physics, Circuit design, VLSI design automation, High-level modeling, Optimization, Computer architecture, Communications and Digital Signal Processing.<br /><br />   

The duration of the VDTT Masters program is normally 24 months. During the program, the students are awarded a scholarship of Rs.8,000 a month, in addition to a contingency of Rs.10,000 a year for travel, books and other consumable. The study currently includes hands-on exposure to unit fabrication processes at CEERI, Pilani or CARE, IIT Delhi. The student is also required to complete a dissertation on a topic sugested by his or her sponsor, under the joint supervision of a IIT Delhi faculty and sponsor's mentor. Work on the Masters thesis is undertaken in two parts, for one semester at IIT Delhi and in the final 6 months of the program at the sponsor's site.
