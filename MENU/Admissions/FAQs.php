            <a name="Q11" id="Q11"><strong>Okay, what are the course contents?</strong>    </a><br /><br />
            As the name suggests, it is a course which exposes a student to various research areas of VLSI. Its' for anybody who wants to get into VLSI Design, VLSI Design Tools or VLSI Technology. The core courses expose you to various aspects of VLSI Design, Algorithms involved in VLSI CAD tools and IC Fabrication Technology. The course electives allow you to plan the courses during first and second semester. Students also undergo a practical training on IC Fabrication at CEERI Pilani after their second semester.<br />
