        <p align="justify"><a name="Q1" id="Q1"><strong>What is VDTT ?</strong>    </a><br /><br />
  VDTT stands for VLSI Design Tools and Technology. Its' a Masters program at IIT, Delhi.We'll use this acronym through out this FAQ. And if you have just heard about a VLSI course at IIT Delhi, then this is the one of them. :)   This is an interdisciplinary program jointly run by two departments, Electrical Engg and Computer Science &amp; Engg together with the Centre for Applied Research in Electronics. <br />
            <a href="#top">Back to Top</a></p><br />
            <p align="justify"><a name="Q2" id="Q2"><strong>How is it different from other MTech programs ?</strong>    </a><br /><br />
            Its' an inter disciplinary Industry Sponsored program. Each student in this program is sponsored by a particular company. He will also get an opportunity to interact with his sponsoring company along with International career opportunities.<br />
            <a href="#top">Back to Top</a></p><br />
            <p align="justify"><a name="Q3" id="Q3"><strong>What is Inter disciplinary?</strong>    </a><br /><br />
            This means that 3 departments Computer Science &amp; Engg, Electrical Engg and CARE ( Center for Applied Research in Electronics) participate in this program.Students will also undergo a hands-on fabrication training at CEERI, Pilani or at CARE.<br />
            <a href="#top">Back to Top</a></p><br />
            <p align="justify"><a name="Q4" id="Q4"><strong>I am not too comfortable with Electronics but I would like to do VLSI at the system level. Do I have a choice of subjects?</strong>    </a><br /><br />
            The VDTT Program has three streams. Please refer to the <a href="../Courses.php">COURSES</a> section on website for more details.<br />
            <a href="#top">Back to Top</a></p><br />
            <p align="justify"><a name="Q24" id="Q24"><strong>I am confused..?</strong>    </a><br /><br />
