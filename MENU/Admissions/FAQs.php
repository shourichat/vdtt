            <a name="Q16" id="Q16"><strong>What's the selection procedure?</strong>    </a><br /><br />
            Good question. Lets' take time and try to understand this.<br /></p>
                <ol>
                  <li>First you will have to procure the admission forms (Regular ME/MTech Admission-forms issued by IIT, Delhi).</li>
                    <li>You make sure that the duly completed form reaches the institute before the specified deadline.</li>
                    <li>Depending on the number of applications received that year a particular number of students will be short listed for the interviews while a few students may be selected for direct admission. You will be intimated about these things if you meet the basic requirements and any of criteria given below:</li>
                    <li>Exceptional GATE Score with more than 75 % aggregate in the qualifying degree you may be eligible for Direct Admission. This means that you will not be attending the first round of interviews.</li>
                    <li>A GATE score above a certain cutoff decided based on the number of applications received that year you will be called for the first round of interviews.</li>
                    <li>You will be intimated about the date and the venue for the interview.</li>
                    <li>On the specified day you attend the interview along with other candidates. This will be conducted by the VDTT faculty only and based on the performance of the candidates, few of them will be short listed for the second round of interview. This list will be announced on the same day as soon as all the candidates are interviewed.</li>
                    <li>The previously short listed students along with the direct admission candidates will have to meet the representatives from the sponsoring companies. Usually each representative presents ideas and areas of interest about his company. This will help you to provide options for the company you want to be sponsored by.</li>
                    <li>Depending on your options you will be interviewed by the sponsor. Based on your performance and choice you will be selected and alloted a particular sponsor.</li>
            </ol>
                <p>Hope that more or less clears the procedure! .<br />
